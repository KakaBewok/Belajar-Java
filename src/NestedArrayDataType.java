public class NestedArrayDataType {
    public static void main(String[] args) {
        String[][] members = {
                {"Zayn", "Malik"}, //0
                {"Harry", "Styles"}, //1
                {"Lionel", "Messi"} //2
        };

        System.out.println(members[1][0]); //"Harry"
    }
}
