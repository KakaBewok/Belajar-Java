public class UnaryOperator {
    public static void main(String[] args) {
        int angka = 100;

        ++angka; //101, atau angka++
        ++angka; //102
        ++angka; //103

        --angka; //102

        System.out.println(angka);
    }
}
//Unary operator biasa diletakan di depan variabel (++i).
// ++a sama dengan a = a + 1.
